#include "libft.h"

void		ft_putbits_fd(unsigned char c, int fd)
{
	unsigned char	b;

	b = (char)128;
	while (b)
	{
		ft_putchar_fd(c & b ? '1' : '0', fd);
		b = b >> 1;
	}
}
