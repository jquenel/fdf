#include "libmat.h"

t_mx4		ft_mx4_new(t_v3d a, t_v3d b, t_v3d c)
{
	t_mx4	m;

	m.mat[0][0] = a.x;
	m.mat[0][1] = a.y;
	m.mat[0][2] = a.z;
	m.mat[0][3] = 0;
	m.mat[1][0] = b.x;
	m.mat[1][1] = b.y;
	m.mat[1][2] = b.z;
	m.mat[1][3] = 0;
	m.mat[2][0] = c.x;
	m.mat[2][1] = c.y;
	m.mat[2][2] = c.z;
	m.mat[2][3] = 0;
	m.mat[3][0] = 0;
	m.mat[3][1] = 0;
	m.mat[3][2] = 0;
	m.mat[3][3] = 1;
	return (m);
}
