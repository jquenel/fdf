#include "libmat.h"

t_v3d				ft_v3d_normalize(t_v3d v)
{
	v.x /= ft_v3d_mag(v);
	v.y /= ft_v3d_mag(v);
	v.z /= ft_v3d_mag(v);
	return (v);
}
